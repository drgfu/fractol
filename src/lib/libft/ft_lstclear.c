#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*prev;

	prev = *lst;
	while (*lst)
	{
		*lst = (*lst)->next;
		del(prev);
		prev = *lst;
	}
}
